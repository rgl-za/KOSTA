SELECT e.FIRST_NAME, e.SALARY, REPLACE (e.PHONE_NUMBER, '.', '-'), TO_CHAR(e.HIRE_DATE, 'YYYY-MM')
FROM EMPLOYEES e 
WHERE HIRE_DATE < '2003-12-31';
